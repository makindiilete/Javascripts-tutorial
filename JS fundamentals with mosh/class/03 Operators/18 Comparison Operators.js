/*We use this to compare the value of a variable with something else. This is divided into two categories : Relational & Equality operators

//RELATIONAL
> : greater than
>= : greater than or equal to
< : less than
<= less than or equal to

//EQUALITY
=== : equals to
!== : not equals to
*/

let x = 1;

//Relational
console.log(x > 0);
console.log(x >= 1);
console.log(x < 1);
console.log(x >= 1);

//Equality
console.log(x === 1);
console.log(x !== 1);

